#include <stdio.h>

int main(void)
{

   // declarations (объявления переменных)
   int a, b, c;
   float e, f, g;

   // statements (инструкции)
   a = 1;
   b = 2;
   c = 3;

   e = 1.0f;
   f = 2.0f;
   g = 3.0f;

   printf("e/a = %.10f\n", e/a);
   printf("f/b = %.10f\n", f/b);
   printf("g/c = %.10f\n", g/c);

   return 0;
}
