#ifndef MATHS_H
#define MATHS_H

/*
   Эта функция складывает два целых числа - a и b
*/
int sum(int a, int b);

/*
   Эта функция вычитает из числа a число b
*/
int sub(int a, int b);

/*
   Эта функция умножает число a на число b
*/
int mul(int a, int b);

/*
   Эта функция делит число a на число b
*/
int div(int a, int b);

#endif

